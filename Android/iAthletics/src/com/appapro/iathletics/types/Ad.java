package com.appapro.iathletics.types;

public class Ad
{
	private String adUrl;		// this is the URL to go to when clicked
	private String imageUrl;	// the URL if the ad's image
	/**
	 * @return the adUrl
	 */
	public String getAdUrl()
	{
		return adUrl;
	}
	/**
	 * @param adUrl the adUrl to set
	 */
	public void setAdUrl(String adUrl)
	{
		this.adUrl = adUrl;
	}
	/**
	 * @return the imageUrl
	 */
	public String getImageUrl()
	{
		return imageUrl;
	}
	/**
	 * @param imageUrl the imageUrl to set
	 */
	public void setImageUrl(String imageUrl)
	{
		this.imageUrl = imageUrl;
	}
	
	

}
